class Solution {
public:
    vector<vector<int>> permute(vector<int>& nums) {
        vector<vector<int>> res;
        sort(nums.begin(), nums.end());
        res.push_back(nums);
        int last = nums.size();
        bool success = true;
        while (success) {
            success = false;
            for (int i = last - 2; i >= 0; i--) {
                if (nums[i] < nums[i + 1]) {
                    for (int j = last - 1; j >= i + 1; j--) {
                        if (nums[j] > nums[i]) {
                            swap(nums[i], nums[j]);

                            break;
                        }
                    }

                    reverse(next(nums.begin(), i + 1), nums.end());
                    res.push_back(nums);
                    success = true;
                    break;
                }
            }
        }

        return res;
    }
};

